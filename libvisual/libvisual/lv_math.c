#include "config.h"
#include "lv_math.h"
#include "lv_common.h"
#include "lv_bits.h"
#include "lv_cpu.h"
#include <math.h>

int visual_math_is_power_of_2 (int n)
{
	return (n > 0) && !(n & (n - 1));
}

unsigned int visual_math_round_power_of_2 (unsigned int n)
{
    n--;
	n |= n >> 1;
	n |= n >> 2;
	n |= n >> 4;
	n |= n >> 8;
	n |= n >> 16;
#if SIZEOF_INT > 4
	n |= n >> 32;
#endif
	n++;

	return n;
}
